		data() {
			return {
				id: '',
				type: '',
			
			
				ro:{
					smokingstatus : false,
					drinkingfrequency : false,
					eatinghabits : false,
					bloodsugarlevel : false,
					environmentalpollutionindex : false,
					diagnosticresults : false,
				},
			
				ruleForm: {
					smokingstatus: '',
					drinkingfrequency: '',
					eatinghabits: '',
					bloodsugarlevel: '',
					environmentalpollutionindex: '',
					diagnosticresults: '',
				},

				rules: {
					smokingstatus: [
					],
					drinkingfrequency: [
					],
					eatinghabits: [
					],
